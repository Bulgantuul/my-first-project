// sum.js файлыг импортлон авч байна
const sum = require("./sum");

// --- Шаардлага #1-ийн Тест ---
test("2 тооны нийлбэр зөв гарах ёстой (Positive Case)", () => {
  // 5 + 3 = 8 гэсэн шалгалт
  expect(sum(5, 3)).toBe(8);
});

test("Тэг утга болон сөрөг тоог нэмэх", () => {
  // 0 + (-10) = -10 гэсэн шалгалт
  expect(sum(0, -10)).toBe(-10);
});

// --- Шаардлага #2-ын Тест ---
test("Тоо биш өгөгдөл өгвөл алдаа (Error) заах ёстой", () => {
  // expect(() => ...).toThrow() функц нь функц дуудагдахад алдаа зааж буйг шалгана

  // Эхний аргумент нь string байвал
  expect(() => sum("1", 2)).toThrow("Зөвхөн тоон утга шаардлагатай");

  // Хоёр дахь аргумент нь undefined байвал
  expect(() => sum(1, undefined)).toThrow("Зөвхөн тоон утга шаардлагатай");

  // Аль аль нь тоо биш байвал
  expect(() => sum("a", "b")).toThrow("Зөвхөн тоон утга шаардлагатай");
});
